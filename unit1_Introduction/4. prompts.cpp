#include <iostream>
using namespace std;
int main()
{
	int x=0,y=0;
	cout<<"Please enter two numbers, and seperate them with a space:";
	cin>>x>>y;
	cout<<"The sum of the two numbers is: "       <<x+y<<"\n";
	cout<<"The difference of the two numbers is: "<<x-y<<"\n";
	cout<<"The product of the two numbers is: "   <<x*y<<"\n";
	cout<<"The quotient of the two numbers is: "  <<x/y<<"\n";
	cout<<"The remainder of the two numbers is: " <<x%y<<"\n";
	
	return 0;
}
